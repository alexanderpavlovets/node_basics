
// MVC = Model View Controller 

/*
  Main goal - separate areas of responsibility 
  Model = data storage 
  View = UI for user (visula representation of data stored in Model)
  Controller = logic layer between Model and View 
*/

/*
  Models stores the data
  Views observe the models and changes accordingly 
  Controllers are responsible for modifying Models, based on changes in Views

  User change smth on UI -> controller changes Model -> View sees Model change and react accordingly
*/

